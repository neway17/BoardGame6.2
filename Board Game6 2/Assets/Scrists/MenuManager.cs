    public void loadPack(int i)
    {
        int sum = 0;

        if (i > 0)
            for (int k = 1; k <= 30; k++)
                sum += PlayerPrefs.GetInt("" + (i * 30 - 30 + k) + "stars");
        else sum = 90;

        if (sum >= 60)
        {
            PlayerPrefs.SetInt("levelPack", i);
            SceneManager.LoadScene(2);
        }
    }

    public void reset()
    {
        for (int i = 1; i < 91; i++)
            PlayerPrefs.SetInt(i + "stars", 0);
    }

    public void Play()
    {
        if (0 < PlayerPrefs.GetInt("1stars"))
            SceneManager.LoadScene(1);
        else SceneManager.LoadScene(3);
    }
